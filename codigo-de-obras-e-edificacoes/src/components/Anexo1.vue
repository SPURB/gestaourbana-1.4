		<h3 class="subsecao_lei">5 - DAS CONDIÇÕES DE AERAÇÃO E INSOLAÇÃO </h3>
		<p class="corpo_decreto"><strong class="artigo-decreto">5.A. </strong>A implantação no lote de qualquer edificação, obra complementar, mobiliário, instalação e equipamento, além do atendimento das disposições do PDE, da LPUOS e do COE, e especialmente do item 5 do Anexo I do COE, deverão observar as regras fixadas por este decreto. </p>
		<p class="corpo_lei"><strong class="artigo-lei">5.1. </strong>A edificação, instalação ou equipamento, a partir de 10 m (dez metros) de altura em relação ao perfil natural do terreno deve observar afastamento contínuo, lateral e de fundo, que pode ser escalonado, e que deve ser dimensionado de acordo com fórmula a seguir, respeitado o mínimo de 3 m (três metros).</p>
		<p class="formula"><span class="lei-formula">A</span> = (<span class="lei-formula">H</span> − 6) ÷ 10</p>
		<p class="corpo_lei">onde:</p>
		<p class="formula"><span class="lei-formula">A</span> = afastamento lateral e de fundo;</p>
		<p class="formula"><span class="lei-formula">H</span> = altura da edificação em metros contados a partir do perfil do terreno.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.1. </strong>Para a aplicação do item 5.1 do Anexo I do COE, a altura “H” da edificação medida em metros, será contada a partir da cota de nível mais baixa do perfil natural do terreno referente ao plano de fachada considerado, até a cota de nível mais alta da edificação, podendo ser adotado o escalonamento da edificação. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.1.1. </strong>Para efeito de aplicação do cálculo da altura “H” da edificação, serão adotadas as cotas e curvas de nível indicadas no Levantamento Topográfico elaborado pelo responsável técnico. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.1.2. </strong>No cálculo da altura “H” da edificação não serão considerados: </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> platibandas que envolve o telhado e o guarda corpo de proteção contra queda com até 1,20 m (um metro e vinte); </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> anteparos verticais como gradis, alambrados ou similar que apresentem superfície vazada igual ou superior a 80% de sua superfície total; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">III -</strong> ático. </p>
		<p class="corpo_lei"><strong class="artigo-lei">5.2. </strong>A distância mínima obrigatória entre blocos de uma mesma edificação é igual à soma dos afastamentos de cada bloco calculados conforme item 5.1 deste Código.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.2. </strong>A distância mínima obrigatória entre blocos de uma mesma edificação é igual à soma dos afastamentos de cada bloco calculados conforme item 5.1 do Anexo I do COE. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.2.1. </strong>Quando os blocos de uma mesma edificação forem compostos pelo mesmo embasamento, a altura “H” será contada a partir da cota de nível do piso de laje de cada bloco. </p>
		<p class="corpo_lei"><strong class="artigo-lei">5.3. </strong>Observadas as normas pertinentes, a aeração e a insolação naturais dos compartimentos podem ser proporcionadas pelos seguintes espaços, para os quais as aberturas devem estar voltadas:</p>
		<p class="corpo_lei"><strong class="destaque-lei">I -</strong> recuo obrigatório previsto na LPUOS;</p>
		<p class="corpo_lei"><strong class="destaque-lei">II -</strong> espaço do logradouro;</p>
		<p class="corpo_lei"><strong class="destaque-lei">III -</strong> afastamento previsto no item 5.1 e 5.2 deste Código;</p>
		<p class="corpo_lei"><strong class="destaque-lei">IV -</strong> área livre descoberta interna.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.3. </strong>Nos casos de aeração e insolação naturais, proporcionadas através do Espaço do Logradouro estabelecido no item 5.3 do Anexo I do COE, com ou sem a previsão de recuo de frente, o afastamento “A” não poderá ultrapassar a distância entre a face da edificação e o eixo do logradouro. </p>
		<p class="corpo_lei"><strong class="destaque-lei">5.3.1. </strong>A área livre interna ao lote deve apresentar as seguintes dimensões de acordo com a altura “H” da edificação contada do perfil do terreno:</p>
		<p class="corpo_lei"><strong class="destaque-lei">I -</strong> quando “H” igual ou inferior a 10 m (dez metros): área mínima de 5,00 m² (cinco metros quadrados) e largura mínima de 1,50 m (um metro e cinquenta centímetros);</p>
		<p class="corpo_lei"><strong class="destaque-lei">II -</strong> quando “H” superior a 10 m (dez metros): dimensão correspondente a um retângulo com lados iguais ou superiores, respectivamente, a “2A” por “3A”.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.4. </strong>A área livre descoberta interna ao lote corresponde ao poço interno descoberto da edificação e deverá apresentar as seguintes dimensões de acordo com a altura “H” da edificação contada do perfil natural do terreno: </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> área mínima de 5,00 m² (cinco metros quadrados) e largura mínima de 1,50 m (um metro e cinquenta centímetros), quando “H” for igual ou inferior a 10 m (dez metros); </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> retângulo conforme o inciso II do item 5.3.1 do Anexo I do COE, situação em que poderá ser adotado o escalonamento, quando “H” for superior a 10 m (dez metros). </p>
		<p class="corpo_lei"><strong class="artigo-lei">5.4. </strong>No caso de área sujeita a diretrizes urbanísticas próprias, operação urbana ou outra situação onde haja a dispensa da observância dos recuos previstos no PDE e LPUOS, fica dispensado o atendimento ao afastamento “A” estabelecido neste Código, sendo a edificação insolada e aerada pelo espaço do logradouro público.</p>
		<p class="corpo_lei"><strong class="artigo-lei">5.5. </strong>Quando houver edificação vizinha implantada sem atendimento de recuo nas divisas laterais e de fundo, admite-se a justaposição da nova edificação à edificação lindeira, sem prejuízo dos índices de ocupação e aproveitamento previstos na LPUOS.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.5. </strong>Quando houver edificação vizinha implantada sem o atendimento de recuo nas divisas laterais e de fundos, admite-se a justaposição da nova edificação à edificação lindeira existente, sem prejuízo do atendimento aos índices de aproveitamento, dimensionamento e ocupação previstos pela LPUOS. </p>
		<p class="corpo_lei"><strong class="destaque-lei">5.5.1. </strong>A justaposição fica dispensada da análise específica de órgão técnico quando, no trecho da divisa onde ocorrer a justaposição, a altura da nova edificação não ultrapassar a altura da edificação lindeira existente.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.5.1. </strong>A justaposição fica dispensada da análise específica de órgão técnico competente quando, no trecho da divisa onde ocorrer a justaposição, a altura da nova edificação não ultrapassar a altura da edificação lindeira existente. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.5.2. </strong>Quando adotada a justaposição e a altura da nova edificação ultrapassar a altura da edificação lindeira existente, deverá ser observado o afastamento “A” e altura “H” a partir do topo da edificação existente, no trecho da justaposição, admitindo-se o escalonamento. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">5.A.6. </strong>Nas edificações novas, os compartimentos e ambientes deverão ser posicionados e dimensionados de forma a proporcionar conforto ambiental, térmico, acústico, e proteção contra a umidade, obtidos pelo adequado dimensionamento e emprego dos materiais das paredes, cobertura, pavimento e aberturas, bem como das instalações e equipamentos conforme dimensões mínimas elencadas na <strong>tabela abaixo</strong>.</p>

