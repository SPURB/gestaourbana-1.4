		<p class="corpo_lei"><strong class="destaque-lei">VI - </strong>alteração do uso do projeto de edificação.</p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 60. </strong>O prazo para a interposição de recurso é de 30 (trinta) dias a contar da data da publicação do despacho de indeferimento, após o qual o processo deve ser arquivado, sem prejuízo da ação fiscal correspondente e cobrança das taxas devidas.</p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 61. </strong>Os prazos fixados neste Código são contados em dias corridos, a partir do primeiro dia útil após o evento de origem até o seu dia final inclusive.</p>
		<p class="corpo_lei"><strong class="destaque-lei">Parágrafo único. </strong>Caso não haja expediente no dia final do prazo, prorroga-se automaticamente o seu término para o dia útil imediatamente posterior.</p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 62. </strong>Os prazos de validade do Alvará de Aprovação e do Alvará de Execução ficam suspensos enquanto perdurar qualquer um dos seguintes impedimentos ao início ou prosseguimento da obra:</p>
		<p class="corpo_lei"><strong class="destaque-lei">I - </strong>decisão judicial determinando ou que implique a paralisação ou o não início da obra;</p>
		<p class="corpo_lei"><strong class="destaque-lei">II - </strong>calamidade pública;</p>
		<p class="corpo_lei"><strong class="destaque-lei">III - </strong>declaração de utilidade pública ou interesse social.</p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 63. </strong>Os documentos de controle da atividade edilícia de que trata este Código podem, enquanto vigentes, a qualquer tempo, mediante ato da autoridade competente, ser:</p>
		<p class="corpo_lei"><strong class="destaque-lei">I - </strong>revogados, atendendo a relevante interesse público;</p>
		<p class="corpo_lei"><strong class="destaque-lei">II - </strong>cassados, em caso de desvirtuamento, por parte do interessado, da licença concedida ou de descumprimento de exigência estabelecida em sua emissão;</p>
		<p class="corpo_lei"><strong class="destaque-lei">III - </strong>anulados, em caso de comprovação de ilegalidade em sua expedição.</p>
		<p class="corpo_decreto"><strong class="artigo-decreto">Art. 53. </strong>A cassação e a anulação de Alvarás de Aprovação e de Execução de edificações caberão aos Coordenadores da SMUL e aos Coordenadores de Planejamento e Desenvolvimento Urbano das PRs, no âmbito de suas respectivas competências, na forma estabelecida no COE e neste decreto.<strong class="destaque-decreto"></strong></p>
		<p class="corpo_decreto"><strong class="destaque-decreto">Parágrafo único. </strong>Outros documentos de controle de atividade edilícia poderão ser declarados nulos, observando-se o mesmo procedimento para a anulação de Alvará estabelecido no COE e neste decreto. </p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 64. </strong>O Certificado de Conclusão, o Certificado de Regularização, o Certificado de Acessibilidade e o Certificado de Segurança perdem sua eficácia caso ocorram alterações de ordem física no imóvel em relação às condições regularmente aceitas pela Prefeitura.</p>
		<p class="corpo_decreto"><strong class="artigo-decreto">Art. 54. </strong>Após a emissão do Certificado de Conclusão ou do Certificado de Regularização de edificação, quando constadas alterações físicas no imóvel ou o desvirtuamento do documento emitido, a edificação será cadastrada como irregular. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">Parágrafo único. </strong>No caso previsto no “caput” deste artigo, a regularização da edificação dependerá da emissão de novo documento de atividade edilícia, na forma prevista no COE e neste decreto.</p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 65. </strong>O Alvará de Autorização é expedido a título precário, podendo ser cancelado a qualquer tempo por desvirtuamento de seu objeto ou desinteresse em sua manutenção ou revalidação.</p>
		<p class="corpo_decreto"><strong class="artigo-decreto">Art. 55. </strong>O Alvará de Autorização poderá ser cancelado por despacho da autoridade competente para a sua emissão. </p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 66. </strong>O cadastro de equipamentos perde a eficácia caso ocorra alteração de ordem física no equipamento em relação às condições regularmente cadastradas na Prefeitura ou caso a respectiva renovação não seja solicitada dentro do prazo legal.</p>
		<p class="corpo_decreto"><strong class="artigo-decreto">Art. 56. </strong>O Cadastro e Manutenção de Equipamentos perderá sua validade quando ocorrer uma das seguintes hipóteses: </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> comprovação da falsidade ou erro das informações prestadas; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> modificações nas características da edificação, no uso, ou na atividade que implique em alteração das regras de segurança; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">III -</strong> decurso de seu prazo; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">IV -</strong> troca do equipamento já cadastrado, exceto quando relativo a sistema de segurança. </p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 67. </strong>Constatada a qualquer tempo a não veracidade das declarações apresentadas nos pedidos de que trata esta lei, aplicam-se, ao proprietário ou possuidor e profissionais envolvidos, as penalidades administrativas previstas neste Código, sem prejuízo das sanções criminais cabíveis.</p>
		<p class="corpo_lei"><strong class="destaque-lei">§ 1º </strong>A atuação irregular do profissional deve ser comunicada ao órgão federal fiscalizador do exercício profissional.</p>
		<p class="corpo_lei"><strong class="destaque-lei">§ 2º </strong>Caso haja elementos que indiquem a prática de infração penal, a Prefeitura comunicará o fato à autoridade policial competente.</p>
		<p class="corpo_lei"><strong class="artigo-lei">Art. 68. </strong>Caso se tenha notícia de fato que possa ensejar a cassação ou anulação do documento expedido, nos termos dos incisos II e III do art. 63 deste Código, a Prefeitura deve notificar o interessado para a apresentação de defesa no prazo de 30 (trinta) dias, de forma a garantir o contraditório e a ampla defesa, podendo, na defesa, comprovar ter sido sanada a irregularidade.</p>
		<p class="corpo_lei"><strong class="destaque-lei">§ 1º </strong>Por motivo relevante ou para evitar prejuízo de difícil reparação, a Prefeitura pode suspender os efeitos do documento emitido até decisão sobre sua anulação ou cassação.</p>
		<p class="corpo_lei"><strong class="destaque-lei">§ 2º </strong>Decorrido o prazo para defesa, a Prefeitura pode efetuar as diligências cabíveis e pedir esclarecimentos a outro órgão público envolvido.</p>
		<p class="corpo_lei"><strong class="destaque-lei">§ 3º </strong>No prazo de 30 (trinta) dias, a autoridade deve decidir a respeito da anulação ou cassação do documento.</p>
		<p class="corpo_decreto"><strong class="artigo-decreto">Art. 57. </strong>Caso se tenha notícia de fato que possa ensejar a cassação ou anulação de Alvará de Aprovação ou de Execução de edificação, nos termos dos incisos II e III do artigo 63 do COE, a Coordenadoria competente notificará o interessado para apresentar defesa no prazo de 30 (trinta) dias contados da data da publicação da chamada no DOC, de forma a garantir o contraditório e a ampla defesa, podendo, na defesa, comprovar ter sido sanada a irregularidade. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">§ 1º </strong>Por motivo relevante ou para evitar prejuízo de difícil reparação, será proferido despacho suspendendo os efeitos do Alvará de Aprovação ou do Alvará de Execução de edificação até a decisão sobre sua anulação ou cassação. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">§ 2º </strong>Decorrido o prazo para defesa, a Prefeitura poderá efetuar as diligências cabíveis e pedir esclarecimentos a outro órgão público envolvido. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">§ 3º </strong>No prazo de 30 (trinta) dias, a autoridade deverá decidir a respeito da anulação ou cassação do Alvará de Aprovação ou do Alvará de Execução de edificação. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">§ 4º </strong>Do despacho que anular ou cassar o alvará, caberá um único recurso no prazo de 30 (trinta) dias, contados de sua publicação no DOC, a ser apreciado pelo Secretário Municipal de Urbanismo e Licenciamento ou Prefeito Regional, no âmbito de suas respectivas competências. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">§ 5º </strong>A decisão do recurso pelo Secretário Municipal de Urbanismo e Licenciamento ou Prefeito Regional encerra definitivamente a instância administrativa a respeito de declaração de nulidade, anulação ou cassação do alvará. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">§ 6º </strong>As notificações e comunicações de decisões de que tratam este artigo serão publicadas no DOC e poderão ser enviadas ao interessado por meio do endereço eletrônico cadastrado no processo. </p>
