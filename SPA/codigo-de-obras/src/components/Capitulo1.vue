	<h1>DAS DISPOSIÇÕES GERAIS</h1>
		
		<p><strong>LEI Nº 16.642/17</strong></p>
		<p>Aprova o Código de Obras e Edificações do Município de São Paulo; introduz alterações nas Leis nº 15.150, de 6 de maio de 2010, e nº 15.764, de 27 de maio de 2013.</p>
		<p><strong>JOÃO DORIA</strong>, Prefeito do Município de São Paulo, no uso das atribuições que lhe são conferidas por lei, faz saber que a Câmara Municipal, em sessão de 1º de julho de 2016, decretou e eu promulgo a seguinte lei:</p>
		<div class="decreto">
			<p><strong>DECRETO Nº 57.776/17</strong>*</p>
			<p>Regulamenta a Lei nº 16.642, de 9 de maio de 2017, que aprovou o Código de Obras e Edificações do Município de São Paulo; define os membros da Comissão de Edificações e Uso do Solo – CEUSO.</p>
			<p><strong>JOÃO DORIA</strong>, Prefeito do Município de São Paulo, usando das atribuições que lhe são conferidas por lei, e CONSIDERANDO as disposições dos artigos 122 e 123 do Código de Obras e Edificações do Município de São Paulo, DECRETA:</p>
			<p align="right">* Com as retificações do Decreto nº 57.815/17 consideradas.</p>
		</div>

		<hr />

		<p><strong>Art. 1º</strong> Fica aprovado o Código de Obras e Edificações do Município de São Paulo – COE, que disciplina, no Município de São Paulo, as regras gerais a serem observadas no projeto, no licenciamento, na execução, na manutenção e na utilização de obras, edificações e equipamentos, dentro dos limites do imóvel, bem como os respectivos procedimentos administrativos, executivos e fiscalizatórios, sem prejuízo do disposto na legislação estadual e federal pertinente.</p>
		<div class="decreto">
			<strong class="artigo-decreto">Art. 1º </strong>Este decreto regulamenta os procedimentos administrativos e executivos e fixa as regras gerais e específicas a serem obedecidas no projeto, licenciamento, execução, manutenção, utilização e fiscalização de obras, edificações, equipamentos, obras complementares, terraços e mobiliários, dentro dos limites dos imóveis em que se situam, nos termos do disposto na Lei n.º 16.642, de 9 de maio de 2017, que aprovou o Código de Obras e Edificações do Município de São Paulo – COE.</p>
			<p><strong>§ 1º </strong>Integram este decreto os anexos a seguir discriminados</p>
			<p><strong>I -</strong> Anexo I – Disposições Técnicas;</p>
			<p><strong>II -</strong> Anexo II – Desenhos;</p>
			<p><strong>III -</strong> Anexo III – Tabela de Multas;</p>
			<p><strong>IV - </strong>Anexo IV – Tabelas Relativas às Áreas Não Computáveis.</p>
			<p><strong>§ 2º </strong>Os modelos de projeto simplificado, quadros, legendas, documentos e declarações para instruir cada um dos pedidos de emissão de documentos de atividade edilícia de que trata o COE serão fixados mediante portaria da Secretaria Municipal de Urbanismo e Licenciamento – SMUL.</p>
			<strong>Art. 2º </strong>Para os fins deste decreto, os órgãos, entidades e expressões serão identificados pelas seguintes siglas ou abreviaturas</p>
			<p><strong>I - </strong>ART – Anotação de Responsabilidade Técnica perante o CREA;</p>
			<p><strong>II - </strong>AVCB – Auto de Vistoria do Corpo de Bombeiros;</p>
			<p><strong>III - </strong>CAU – Conselho de Arquitetura e Urbanismo;</p>
			<p><strong>IV - </strong>CCIR – Certificado de Cadastro de Imóvel Rural;</p>
			<p><strong>V - </strong>CEPAC – Certificado de Potencial Adicional Construtivo;</p>
			<p><strong>VI - </strong>COE – Código de Obras e Edificações;</p>
			<p><strong>VII - </strong>CREA– Conselho Regional de Engenharia e Agronomia;</p>
			<p><strong>VIII - </strong>DIS – Decreto de Declaração de Interesse Social;</p>
			<p><strong>IX - </strong>DOC – Diário Oficial da Cidade;</p>
			<p><strong>X - </strong>DUP – Decreto de Declaração de Utilidade Pública;</p>
			<p><strong>XI - </strong>ISS – Imposto Sobre Serviços de Qualquer Natureza;</p>
			<p><strong>XII - </strong>IEOS – Intimação para Execução de Obras e Serviços;</p>
			<p><strong>XIII - </strong>IT – Instrução Técnica (Corpo de Bombeiros);</p>
			<p><strong>XIV - </strong>LOE – Legislação de Obras e Edificações, incluindo o COE;</p>
			<p><strong>XV - </strong>LPUOS – Legislação de Parcelamento, Uso e Ocupação do Solo;</p>
			<p><strong>XVI - </strong>NEC – Notificação de Exigências Complementares;</p>
			<p><strong>XVII - </strong>NT – Norma Técnica, expedida por órgão competente, exceto ABNT;</p>
			<p><strong>XVIII - </strong>NTO – Norma Técnica Oficial (registrada na Associação Brasileira de Normas Técnicas – ABNT);</p>
			<p><strong>XIX - </strong>OU – Operação Urbana;</p>
			<p><strong>XX - </strong>OUC – Operação Urbana Consorciada;</p>
			<p><strong>XXI - </strong>PDE – Plano Diretor Estratégico;</p>
			<p><strong>XXII - </strong>PR – Prefeitura Regional</p>
			<p><strong>XXIII - </strong>Prefeitura – Prefeitura do Município de São Paulo;</p>
			<p><strong>XXIV - </strong>RRT – Registro de Responsabilidade Técnica perante o CAU;</p>
			<p><strong>XXV - </strong>SLCe – Sistema de Licenciamento Eletrônico de Construção;</p>
			<p><strong>XXVI - </strong>SMPR – Secretaria Municipal das Prefeituras Regionais;</p>
			<p><strong>XXVII - </strong>SMUL – Secretaria Municipal de Urbanismo e Licenciamento;</p>
			<p><strong>XXVIII - </strong>TEV/COE – Taxa para Exame e Verificação dos Pedidos de Documentos de Controle da Atividade Edilícia.</p>
			<strong>Art. 3º </strong>Enquadram-se como processos especiais, com procedimento definido neste decreto, os pedidos de emissão dos seguintes documentos de controle da atividade edilícia previstos no artigo 12 do COE:</p>
			<p><strong>I - </strong>Alvará de Aprovação;</p>
			<p><strong>II - </strong>Alvará de Execução;</p>
			<p><strong>III - </strong>Projeto Modificativo;</p>
			<p><strong>IV - </strong>Certificado de Conclusão;</p>
			<p><strong>V - </strong>Certificado de Regularização;</p>
			<p><strong>VI - </strong>Certificado de Acessibilidade;</p>
			<p><strong>VII - </strong>Certificado de Segurança;</p>
			<p><strong>VIII - </strong>Alvará de Autorização;</p>
			<p><strong>IX - </strong>Cadastro de Equipamento;</p>
			<p><strong>X - </strong>Manutenção de Equipamento;</p>
			<p><strong>XI - </strong>Ficha Técnica;</p>
			<p><strong>XII - </strong>Diretrizes de Projeto.</p>
			<strong>Art. 4º </strong>Compete à Secretaria Municipal de Urbanismo e Licenciamento a análise e decisão dos pedidos de documentos de controle da atividade edilícia de que tratam o COE e este decreto.</p>
			<p>§ 1º </strong>A competência de análise e decisão poderá ser transferida às PRs e SMPR por decreto, em razão da metragem da área, da lotação ou da categoria de uso.</p>
			<p>§ 2º </strong>Ficam mantidas as delegações de competências às PRs e SMPR, até a revisão dos respectivos decretos de delegação de competência em vigor.</p>
		</div>

		<p><strong>Art. 2º </strong>A análise dos projetos e dos pedidos de documentos de controle da atividade edilícia deve ser efetuada quanto à sua observância:</p>

		<p><strong>I - </strong>às normas do <strong>Plano Diretor Estratégico – PDE</strong><sup>1</sup>, da <strong>Legislação de Parcelamento, Uso e Ocupação do Solo – LPUOS</strong><sup>2</sup> e das <strong>Operações Urbanas Consorciadas – OUC</strong><sup>3</sup>;</p>

		<div class="glossario-box">
			<p><span class="nota">1</span> Plano Diretor Estratégico – PDE (Lei nº 16.050, de 31 de julho de 2014), dispõe sobre a Política de Desenvolvimento Urbano, o Sistema de Planejamento Urbano e o Plano Diretor Estratégico do Município de São Paulo e aplica-se à totalidade do seu território.</p>
			<p><span class="nota">2</span> Lei de Parcelamento, Uso e Ocupação do Solo – LPUOS (Lei nº 16.402, de 22 de março de 2016) Dispõe sobre o parcelamento, o uso e a ocupação do solo no território do Município de São Paulo de acordo com o Plano Diretor Estratégico – PDE, e legislação correlata.</p>
			<p><span class="nota">3</span> Operação Urbana / Operação Urbana Consorciada – OU / OUC são instrumentos cujas diretrizes são previstas pelo PDE e aprovadas mediante lei municipal, realizadas sob a coordenação do Poder Público, envolvendo a iniciativa privada, empresas prestadoras de serviços públicos, moradores e usuários do local, em perímetro delimitado do território a fim de conferir melhorias urbanísticas que atendam às demandas sociais, ambientais e econômicas existentes.</p>
		</div>

		
		<p><strong>II - </strong>aos planos de melhoramento viário aprovados;</p>
		<p><strong>III - </strong>às servidões administrativas;</p>
		<p><strong>IV - </strong>às restrições decorrentes das declarações de utilidade pública e de interesse social;</p>
		<p><strong>V - </strong>às limitações decorrentes do tombamento e da preservação de imóveis;</p>
		<p><strong>VI - </strong>às regras para mitigar o impacto ambiental e de vizinhança;</p>
		<p><strong>VII - </strong>às restrições para a ocupação de áreas com risco ou contaminadas;</p>
		<p><strong>VIII - </strong>a quaisquer leis ou regulamentos relacionados às características externas da edificação ou equipamento e sua inserção na paisagem urbana;</p>
		<p><strong>IX - </strong>às exigências relativas às condições de segurança de uso das edificações com alto potencial de risco de incêndios e situações de emergência.</p>
		<p><strong class="artigo-lei">Art. 3º </strong>Para fins de aplicação das disposições deste Código ficam adotadas as seguintes definições:</p>
		<p><strong>I - </strong>acessibilidade: condição de utilização, com segurança e autonomia, total ou assistida, por pessoa com deficiência ou com mobilidade reduzida, de edificação, espaço, mobiliário e equipamento;</p>
		<p><strong>II - </strong>acessível: edificação, espaço, mobiliário e equipamento que possa ser utilizado e vivenciado por qualquer pessoa, inclusive aquela com deficiência ou mobilidade reduzida, conforme os parâmetros definidos em norma técnica pertinente;</p>
		<p><strong>III - </strong>adaptação razoável: modificações e ajustes necessários e adequados que não acarretem ônus desproporcional ou indevido, quando requeridos em cada caso, a fim de assegurar que as pessoas com deficiência ou mobilidade reduzida possam gozar ou exercer, em igualdade de oportunidades com as demais pessoas, os direitos humanos e liberdades fundamentais;</p>
		<p><strong>IV - </strong>alinhamento: linha de divisa entre o terreno e o logradouro público;<strong></strong></p>
		<p><strong>V - </strong>andar: volume compreendido entre dois pavimentos consecutivos ou entre o pavimento e o nível superior de sua cobertura;</p>
		<p><strong class="bolinha">VI - </strong><a class="ref_ilust" href="#ilust">ático</a>: parte do volume superior de uma edificação, destinada a abrigar casa de máquinas, piso técnico de elevadores, equipamentos, caixa d’água e circulação vertical;</p>
		<p><strong class="bolinha">VII - </strong><a class="ref_ilust" href="#ilust">beiral</a>: prolongamento da cobertura que se sobressai das paredes externas da edificação;</p>
		<p><strong>VIII - </strong>canteiro de obras: espaço delimitado pelo tapume, destinado ao preparo e apoio à execução da obra ou serviço, incluindo os elementos provisórios que o compõem, tais como estande de vendas, alojamento, escritório de campo, depósitos, galeria, andaime, plataforma e tela protetora visando à proteção da edificação vizinha e logradouro público;</p>
		<p><strong>IX - </strong>demolição: total derrubamento de uma edificação;</p>
		<p><strong>X - </strong>edificação: obra coberta destinada a abrigar atividade humana ou qualquer instalação, equipamento e material;</p>
		<p><strong>XI - </strong>edificação transitória: edificação de caráter não permanente, passível de montagem, desmontagem e transporte;</p>
		<p><strong>XII - </strong>embargo: ordem de paralisação dos trabalhos na obra ou serviço em execução sem a respectiva licença ou por desatendimento à Legislação de Obras e Edificações – LOE ou LPUOS;</p>
		<p><strong>XIII - </strong>equipamento: elemento não considerado como área construída, destinado a guarnecer ou completar uma edificação, a ela se integrando, tais como equipamentos mecânicos de transporte, tanques de armazenagem, bombas e sistemas de energia, aquecimento solar e a gás, podendo ser:</p>
		<p><strong>a)</strong> equipamento permanente: equipamento de caráter duradouro;</p>
		<p><strong>b)</strong> equipamento transitório: equipamento de caráter não permanente, passível de montagem, desmontagem e transporte;</p>
		<p><strong>XIV - </strong>interdição: ordem e ato de fechamento e desocupação do imóvel em situação irregular ou de risco em relação às condições de estabilidade, segurança ou salubridade;</p>
		<p><strong>XV - </strong>mobiliário: elemento construtivo que não se enquadra como edificação ou equipamento, tais como:</p>
		<p><strong>a)</strong> guarita e módulo pré-fabricado;</p>
		<p><strong class="bolinha">b)</strong> <a class="ref_ilust" href="#ilust">jirau</a>, elemento constituído de estrado ou passadiço, instalado a meia altura em compartimento;</p>
		<p><strong>c) </strong>abrigo ou telheiro sem vedação lateral em pelo menos 50% (cinquenta por cento) do perímetro;</p>
		<p><strong>d) </strong>estufas, quiosques, viveiros de plantas, churrasqueiras;</p>
		<p><strong>e) </strong>dutos de lareiras;</p>
		<p><strong class="bolinha">f) </strong><a class="ref_ilust" href="#ilust">pérgulas</a>;</p>
		<p><strong>XVI - </strong>movimento de terra: modificação do perfil do terreno ou substituição do solo em terrenos alagadiços ou que implique em alteração topográfica superior a 1,50 m (um metro e cinquenta centímetros) de desnível ou a 1.000,00 m³ (mil metros cúbicos) de volume;</p>
		<p><strong>XVII - </strong>muro de arrimo: muro resistente, que trabalha por gravidade ou flexão, construído para conter maciço de terra, empuxo das águas de infiltração, sobrecarga de construção, sobreaterro e situações similares;</p>
		<p><strong class="bolinha">XVIII - </strong><a class="ref_ilust" href="#ilust">obra complementar</a>: edificação secundária ou parte da edificação que, funcionalmente, complemente a atividade desenvolvida no imóvel, tais como:</p>
		<p><strong>a)</strong> passagem coberta de pedestre sem vedação lateral;</p>
		<p><strong>b)</strong> abrigo de porta e portão, automóvel, lixo, recipiente de gás e entrada e medidores de concessionárias;</p>
		<p><strong>c)</strong> casa de máquina isolada, cabine de força, cabine primária;</p>
		<p><strong>d)</strong> reservatório em geral, elevado e enterrado, chaminé e torre isoladas;</p>
		<p><strong>e) </strong>bilheteria, portaria, caixa eletrônico;</p>
		<p><strong>XIX - </strong>obras de emergência: obras de caráter urgente, essenciais à garantia das condições de estabilidade, segurança ou salubridade do imóvel;</p>
		<p><strong>XX - </strong>pavimento: plano de piso;</p>
		<p><strong>XXI - </strong>pavimento térreo: aquele definido na LPUOS;</p>
		<p><strong>XXII - </strong>pavimento de acesso: aquele definido na LPUOS;</p>
		<p><strong>XXIII - </strong>peça gráfica: representação gráfica de elementos para a compreensão de um projeto ou obra;</p>
		<p><strong>XXIV - </strong>pérgulas: vigas horizontais ou inclinadas, sem cobertura;</p>
		<p><strong>XXV - </strong>pessoa com deficiência: aquela que tem impedimento de longo prazo de natureza física, mental, intelectual ou sensorial, o qual, em interação com uma ou mais barreiras, pode obstruir sua participação plena e efetiva na sociedade em igualdade de condições com as demais pessoas;</p>
		<p><strong>XXVI - </strong>pessoa com mobilidade reduzida: aquela que tenha, por qualquer motivo, dificuldade de movimentação, permanente ou temporária, gerando redução efetiva da mobilidade, da flexibilidade, da coordenação motora ou da percepção, incluindo idoso, gestante, lactante, pessoa com criança de colo e obeso;</p>
		<p><strong>XXVII - </strong>reconstrução: obra destinada à recuperação e recomposição de uma edificação, motivada pela ocorrência de incêndio ou outro sinistro, mantendo-se as características anteriores, observadas as condições de adaptação à segurança de uso e de acessibilidade estabelecidas nesta lei;</p>
		<p><strong class="bolinha">XXVIII - </strong><a class="ref_ilust" href="#ilust">recuo</a>: distância entre o limite externo da edificação e a divisa do lote, medida perpendicularmente a esta;</p>
		<p><strong>XXIX - </strong>reforma: intervenção na edificação que implique alteração da área construída ou da volumetria, com a simultânea manutenção de parte ou de toda a área existente, com ou sem mudança de uso;</p>
		<p><strong>XXX - </strong>reforma sem acréscimo de área: intervenção na edificação sem alteração da área construída, que implique em modificação da estrutura, pé-direito ou compartimentação vertical, com ou sem mudança de uso;</p>
		<p><strong>XXXI - </strong>reparo: obra ou serviço destinado à manutenção de um edifício, sem implicar mudança de uso, acréscimo ou supressão de área, alteração da estrutura, da compartimentação horizontal e vertical, da volumetria, e dos espaços destinados à circulação, iluminação e ventilação;</p>
		<p><strong>XXXII - </strong>requalificação: intervenção em edificação existente, visando à adequação e modernização das instalações, com ou sem mudança de uso;</p>
		<p><strong>XXXIII - </strong>saliência: elemento arquitetônico, engastado ou aposto na edificação ou muro, tais como aba horizontal e vertical, marquise, jardineira, floreira, ornamento e brise;</p>
		<p><strong class="bolinha">XXXIV - </strong><a class="ref_ilust" href="#ilust">tapume</a>: vedação provisória usada durante a construção, visando à proteção de terceiros e ao isolamento da obra ou serviço;</p>
		<p><strong class="bolinha">XXXV - </strong><a class="ref_ilust" href="#ilust">terraço aberto</a>: peça justaposta à edificação, constituída em balcão aberto, sem ou com vedação, desde que retrátil ou vazada do tipo quebra-sol, em balanço ou não, complementar à unidade residencial ou não residencial, não abrigando função essencial ao pleno funcionamento da unidade;</p>

		<p><strong>XXXVI - </strong>uso privado: espaço ou compartimento de utilização exclusiva da população permanente da edificação;</p>
		<p><strong>XXXVII - </strong>uso restrito: espaço, compartimento, ou elemento interno ou externo, disponível estritamente para pessoas autorizadas.</p>
		<p><strong>Parágrafo único. </strong>Ficam também adotadas as seguintes abreviações:</p>
		<p><strong>I - </strong>ART – Anotação de Responsabilidade Técnica perante o CREA;</p>
		<p><strong>II - </strong>CAU – Conselho de Arquitetura e Urbanismo;</p>
		<p><strong>III - </strong>COE – Código de Obras e Edificações;</p>
		<p><strong>IV - </strong>CREA – Conselho Regional de Engenharia e Agronomia;</p>
		<p><strong>V - </strong>LOE – Legislação de Obras e Edificações, incluindo o COE;</p>
		<p><strong>VI - </strong>LPUOS – Legislação de Parcelamento, Uso e Ocupação do Solo;</p>
		<p><strong>VII - </strong>PDE – Plano Diretor Estratégico;</p>
		<p><strong>VIII - </strong>Prefeitura – Prefeitura do Município de São Paulo;</p>
		<p><strong>IX - </strong>RRT – Registro de Responsabilidade Técnica perante o CAU.</p>

		<div class="decreto">
			<strong>Art. 5º </strong>Para fins de aplicação das disposições do COE e deste Código ficam adotadas, além das dispostas em lei, as seguintes definições:</p>
			<strong>I - </strong>baixa de responsabilidade técnica: a ciência, por parte da Prefeitura, do desligamento do profissional responsável pela execução da obra regularmente licenciada, autorizada ou comunicada;</p>
			<strong>II - </strong>assunção de responsabilidade técnica: a aceitação, por parte da Prefeitura, de novo profissional, legalmente habilitado, que passa a responder pela direção técnica relativa à execução de obra regularmente licenciada, autorizada ou comunicada;</p>
			<strong>III - </strong>transferência de responsabilidade técnica: a ocorrência simultânea de baixa e assunção de responsabilidade técnica.</p>
			<strong>IV - </strong>demolição: total ou parcial derrubamento da edificação existente.</p>
