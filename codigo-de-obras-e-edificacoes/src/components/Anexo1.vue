		<p class="corpo_lei"><strong class="artigo-lei">4.1. </strong>Devem atender às condições de acessibilidade às pessoas com deficiência ou mobilidade reduzida estabelecidas no COE e legislação correlata a edificação nova e a edificação existente em caso de sua reforma, requalificação ou regularização, quando destinada a uso:</p>
		<p class="corpo_lei"><strong class="destaque-lei">I -</strong> público, entendido como aquele administrado por órgão ou entidade da Administração Pública Direta e Indireta ou por empresa prestadora de serviço público e destinado ao público em geral;</p>
		<p class="corpo_lei"><strong class="destaque-lei">II -</strong> coletivo, entendido como aquele destinado à atividade não residencial.</p>
		<p class="corpo_decreto"><strong class="artigo-decreto">4.A. </strong>Considera-se, exclusivamente, para fins de acessibilidade: </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> Uso privado: espaço ou compartimento de utilização exclusiva da população permanente da edificação de uso residencial; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> Uso restrito: espaço, compartimento, ou elemento interno ou externo, disponível estritamente para pessoas autorizadas, segundo definições previstas nas NTOs de Acessibilidade. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.A.1. </strong>Pelo menos um dos itinerários que comuniquem horizontal e verticalmente a todas as dependências e serviços da edificação, entre si e com o exterior, deve cumprir os requisitos de acessibilidade. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.A.2. </strong>A construção, as áreas objeto de reforma, a ampliação, a regularização ou a mudança de uso de edificações abertas ao público, de uso público ou privadas de uso coletivo deverão ser executadas de modo a serem acessíveis, observados os parâmetros técnicos estabelecidos na NBR 9050 em vigor ou naquela que vier a substitui-la. </p>
		<p class="corpo_lei"><strong class="destaque-lei">4.1.2. </strong>As áreas comuns da edificação multifamiliar também devem observar as condições de acessibilidade.<strong class="destaque-decreto"></strong></p>
		<p class="corpo_decreto"><strong class="artigo-decreto">4.B. </strong>As edificações residenciais multifamiliares, condomínios e conjuntos habitacionais necessitam ser acessíveis em suas áreas de uso comum, devendo as unidades autônomas acessíveis e adaptáveis estar conectadas às rotas acessíveis. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.1. </strong>A rota acessível é um trajeto contínuo, desobstruído e sinalizado, que conecta os ambientes externos e internos de espaços e edificações. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.2. </strong>A rota acessível poderá coincidir com a rota de fuga. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.3. </strong>O percurso entre o estacionamento de veículos e os acessos deverá compor uma rota acessível. </p>
		<p class="corpo_lei"><strong class="artigo-lei">4.2. </strong>Ficam dispensadas do atendimento das exigências estabelecidas neste item 4:</p>
		<p class="corpo_lei"><strong class="destaque-lei">I -</strong> a edificação residencial unifamiliar, a unidade habitacional no conjunto de habitações agrupadas horizontalmente e a unidade habitacional na edificação de uso multifamiliar;</p>
		<p class="corpo_lei"><strong class="destaque-lei">II -</strong> o espaço e o compartimento de utilização restrita e exclusiva em edificação destinada a uso não residencial;</p>
		<p class="corpo_lei"><strong class="destaque-lei">III -</strong> o espaço onde se desenvolve atividade específica que justifique a restrição de acesso;</p>
		<p class="corpo_lei"><strong class="destaque-lei">IV -</strong> o andar superior de edificação de pequeno porte destinado a uso não residencial.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.4. </strong>Ficam dispensadas do atendimento às exigências das condições de acessibilidade estabelecidas no artigo 40 do COE: </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> a edificação residencial unifamiliar, a unidade habitacional no conjunto de habitações agrupadas horizontalmente e a unidade habitacional na edificação de uso multifamiliar, na forma prevista pela legislação federal aplicável; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> os espaços e compartimentos de utilização restrita e exclusiva, onde não haja permanência humana, caracterizados como espaços, salas ou elementos internos ou externos, disponíveis estritamente para pessoas autorizadas nos termos da NBR 9050, ou outra norma técnica que vier a sucedê-la, tais como casas de máquinas, barriletes, passagem de uso técnico e outros com funções similares; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">III -</strong> o andar superior ou inferior de edificação existente com até dois pavimentos e área construída total de até 150 m² (cento e cinquenta metros quadrados) no pavimento não acessível, destinado ao uso não residencial, desde que a atividade instalada no pavimento contíguo da edificação seja a mesma ou funcionalmente complementar à atividade desenvolvida no pavimento acessível; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">IV -</strong> o espaço destinado ao orador em local de reunião, com dimensões compatíveis ao uso de uma pessoa. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.4.1. </strong>Não se aplica a dispensa de atendimento das condições de acessibilidade prevista no inciso III do item 4.B.4., às seguintes atividades: </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> estabelecimentos bancários e instituições financeiras; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> instituições de ensino de todas as modalidades, etapas e níveis de ensino; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">III -</strong> estabelecimento de prestação de serviços de utilidade ou interesse público. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.4.2. </strong>A dispensa prevista no item 4.B.4 deste decreto não exime a aplicação da Lei Federal nº 13.146/2015 – Lei Brasileira de Inclusão da Pessoa com Deficiência e das normas técnicas de acessibilidade vigentes, em especial a NBR 9050, ou outra norma técnica que vier a sucedê-la. </p>
		<p class="corpo_lei"><strong class="artigo-lei">4.3. </strong>Na reforma e na requalificação da edificação existente, com ou sem mudança de uso, caso haja inviabilidade técnica de atendimento às condições de acessibilidade, deve ser realizada a adaptação razoável, nos termos do regulamento, não podendo ser reduzidas as condições já implantadas.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.5. </strong>Na reforma e requalificação de imóveis, as condições de atendimento à acessibilidade deverão ser atendidas, salvo hipóteses de impraticabilidade técnica, situação em que deverá ser proposto projeto de adaptação razoável. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.5.1. </strong>Entende-se por adaptações razoáveis as adaptações, modificações e ajustes necessários e adequados que não acarretem ônus desproporcional e indevido, quando requeridos em cada caso. O ônus desproporcional caracteriza-se pela impraticabilidade do atendimento à determinação de adaptação da edificação, nos termos do item 3.1.24 da NBR 9050, ou norma técnica que a suceder. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.5.2. </strong>Como justificativa da impraticabilidade do atendimento à determinação de adaptação da edificação, deverão ser apresentados os seguintes documentos assinados pelo proprietário ou possuidor, responsável(eis) técnico(s) pelo projeto e pelos equipamentos, acompanhados das respectivas ART(s) / RRT(s): </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">I -</strong> memorial justificativo das obras propostas; </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">II -</strong> declaração de impraticabilidade do atendimento à determinação da adaptação. </p>
		<p class="corpo_lei"><strong class="artigo-lei">4.4. </strong>A edificação deve ser dotada de rampa com largura mínima de 1,20 m (um metro e vinte centímetros) para vencer desnível entre o logradouro público ou área externa e o piso correspondente à soleira de ingresso, admitida a instalação de equipamento mecânico de transporte permanente para esta finalidade.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.6. </strong>A edificação deverá ser dotada de rampa com largura mínima de 1,20 m (um metro e vinte centímetros) para vencer desnível entre o logradouro público ou área externa e o piso correspondente à soleira de ingresso, admitida a instalação de equipamento mecânico de transporte permanente para esta finalidade. </p>
		<p class="corpo_lei"><strong class="destaque-lei">4.4.1. </strong>O equipamento mecânico de transporte permanente destinado às pessoas com deficiência ou mobilidade reduzida, quando prevista sua instalação, pode ocupar as faixas de recuo de frente, laterais e de fundo, não sendo considerado área computável no cálculo do coeficiente de aproveitamento e da taxa de ocupação.</p>
		<p class="corpo_lei"><strong class="artigo-lei">4.5. </strong>O único ou pelo menos um dos elevadores da edificação deve ser acessível, podendo ser substituído por rampa quando o desnível a vencer for igual ou inferior a 12,00 m (doze metros), observadas as normas pertinentes.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.7. </strong>No mínimo um dos elevadores da edificação deverá ser acessível, podendo ser substituído por rampa quando o desnível a vencer for igual ou inferior a 12,00 m (doze metros), observadas as normas pertinentes. </p>
		<p class="corpo_lei"><strong class="artigo-lei">4.6. </strong>A edificação deve dispor de pelo menos uma instalação sanitária em local acessível e com dimensões adaptadas ao uso por pessoa com deficiência ou mobilidade reduzida, em quantidade e localização adequada ao uso a que se destina.</p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.8. </strong>As edificações novas e as áreas a serem ampliadas ou nas reformas em edificações regularmente existentes deverão dispor de pelo menos uma instalação sanitária em local acessível e com dimensões para o uso por pessoa com deficiência ou mobilidade reduzida, em quantidade e localização adequadas ao uso a que se destina, na proporção prevista na NBR 9050, ou outra norma que venha a substituí-la. </p>
		<p class="corpo_decreto"><strong class="destaque-decreto">4.B.8.1. </strong>O sanitário destinado à pessoa com deficiência ou com mobilidade reduzida deverá ter entrada independente dos demais sanitários públicos ou coletivos podendo ser incluído no cálculo do número mínimo de instalações sanitárias exigidas para a atividade. </p>
